package entity;

public class BanJi {
	private String name;
	private int stuNums;
	private Integer id;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getStuNums() {
		return stuNums;
	}

	public void setStuNums(int stuNums) {
		this.stuNums = stuNums;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

}
